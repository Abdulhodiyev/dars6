SELECT DRIVERS.NAME, SUM(TRIPS.DISTANCE)
FROM DRIVERS INNER JOIN TRIPS ON DRIVERS.ID = TRIPS.DRIVER_ID
WHERE TRIPS.TRIP_DATE BETWEEN '2025-01-01' AND '2025-01-31'
GROUP BY DRIVERS.ID, DRIVERS.NAME;
