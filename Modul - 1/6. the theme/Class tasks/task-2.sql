CREATE TABLE IF NOT EXISTS TRIPS
(
    ID SERIAL PRIMARY KEY,
    DRIVER_ID INT,
    CONSTRAINT FK_DRIVER_ID FOREIGN KEY (DRIVER_ID) REFERENCES DRIVERS(ID),
    TRIP_DATE DATE,
    DISTANCE INT NOT NULL
);

